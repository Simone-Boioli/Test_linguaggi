public class Esercizio3a{
		public static void main(String[]args){
		//primo
		int a = 5;
		int b = 3;
		double r1 = (double) a/b;
		System.out.println(r1);
		
		//secondo
		char c = 'c';
		short s = 5000;
		int r2 = c*s;
		System.out.println(r2);
			
		//terzo
		int i = 6;
		float f = 3.14F;
		float r3 = i+f;
		System.out.println(r3);
			
		//quarto
		double r4 = r1 - r2 - r3;
		System.out.println(r4);
		}
}