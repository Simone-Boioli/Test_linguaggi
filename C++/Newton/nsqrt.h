#ifndef nsqrt_h
#define nsqrt_h

/**
 * Calcola la radice quadrata di un numero x usando il metodo di
 * risoluzione di Newton.
 *
 * @param x valore di input
 * @param epsilon precisione del calcolo
 * @return il valore della radice quadrata di x
 */
double nsqrt(double x, double epsilon);


#endif
